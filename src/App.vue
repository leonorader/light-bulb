<template>
  <main id="app">
    <h1></h1>
    <div :style="{'background-color': lightBulbColor, display: 'block'}">
      <img alt="bulb" src="./assets/bulb-input-dark.png">
    </div>
  </main>
</template>

<script>

  export default {
    name: 'app',
    data() {
      return {
        color: 'yellow'
      }
    },
    computed: {
      lightBulbColor() {
        return this.color
      }
    },
    created() {
      this.$api.get('/bulb/color')
        .then(function(response) {
          this.color = response.data
        }.bind(this))
    }
  }
</script>

